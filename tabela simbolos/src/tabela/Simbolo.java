package tabela;

public class Simbolo {
	String tipo;
	String id;
	
	
	
	public Simbolo(String id, String tipo) {
		super();
		this.tipo = tipo;
		this.id = id;
	}
	
	public String getTipo() {
		return tipo;
	}
	public void setTipo(String tipo) {
		this.tipo = tipo;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	
	
	
	
}
